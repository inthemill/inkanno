package ch.unibe.im2.inkanno;

public class ComplianceException extends Exception {

  private static final long serialVersionUID = 1L;

    public ComplianceException() {
		super();
	}

	public ComplianceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ComplianceException(String arg0) {
		super(arg0);
	}

	public ComplianceException(Throwable arg0) {
		super(arg0);
	}

}
